        //Botão Salvar

        //Pegar as infos e guardar dentro do obj
        Funcoes obj = new Funcoes();

        obj.setCargo(txtNome.getText());
        obj.setAtividades(txtAtividade.getText());        

        //Criando o objeto da camada DAO para cadastro no banco
        FuncoesDAO dao = new FuncoesDAO();
