    public void listarFuncoes() {

        FuncoesDAO dao = new FuncoesDAO();
        List<Funcoes> lista = dao.listarFuncoes();

        //Passar o conteudo da lista para o table Model
        DefaultTableModel dados = (DefaultTableModel) tbfFuncoes.getModel();
        dados.setNumRows(0); //limpa os dados da tabela

        for (Funcoes c : lista) {

            //Definindo ordem que aparece na lista
            dados.addRow(new Object[]{
                c.getCodigo(),
                c.getCargo(),
                c.getAtividades()
            }); //vetor da classe obj

        }
    }

